solve :: [Int] -> Int -> Int
solve coins total = if dp ! total > total
                    then -1
                    else dp ! total
  where
  calc n = 1 + minimum (fmap (at . (n-)) coins)
  at n
    | n == 0 = 0
    | n < 0 = total + 1
    | otherwise = dp ! n
  dp = listArray (1, total) $ fmap calc [1..total]

main :: IO ()
main = do
  (_, x) <- getInt2
  coins <- getInts
  print $ solve coins x


readInt :: C.ByteString -> Int
readInt s = let Just (i,_) = C.readInt s in i :: Int

readInt2 :: C.ByteString -> (Int, Int)
readInt2 u = (a, b)
  where
  Just (a,v) = C.readInt u
  Just (b,_) = C.readInt (C.tail v)

readInts :: C.ByteString -> [Int]
readInts str = readInt <$> BS.split (BSI.c2w ' ') str

getInt :: IO Int
getInt  = readInt <$> C.getLine

getInt2 :: IO (Int, Int)
getInt2 = readInt2 <$> C.getLine

getInts :: IO [Int]
getInts = readInts <$> C.getLine
