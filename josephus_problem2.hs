rotate :: Int -> [a] -> [a]
rotate _ [] = []
rotate n xs = zipWith const (drop n (cycle xs)) xs

rotate' :: Int -> [a] -> [a]
rotate' n xs = rotate (length xs -  n) xs

solve'' :: Int -> [Int] -> [Int]
solve'' k = solve'
  where
    solve' :: [Int] -> [Int]
    solve' xs
      | n <= k = xs
      | otherwise = left ++ right
      where
      n = length xs
      zipped = zip xs $ cycle [0..k]
      left = fst <$> filter ((==k).snd) zipped
      right = solve' other
      other' = fst <$> filter ((/=k).snd) zipped
      other = rotate' (n `mod` (k + 1)) other'

solve :: Int -> Int -> [Int]
solve k n = solve'' k [1..n]

main :: IO ()
main = do
  (n, k) <- getInt2
  let res = solve k n
  putStrLn $ unwords $ show <$> res

readInt :: C.ByteString -> Int
readInt s = let Just (i,_) = C.readInt s in i :: Int

readInt2 :: C.ByteString -> (Int, Int)
readInt2 u = (a, b)
  where
  Just (a,v) = C.readInt u
  Just (b,_) = C.readInt (C.tail v)

readInts :: C.ByteString -> [Int]
readInts str = readInt <$> BS.split (BSI.c2w ' ') str

getInt :: IO Int
getInt  = readInt <$> C.getLine

getInt2 :: IO (Int, Int)
getInt2 = readInt2 <$> C.getLine

getInts :: IO [Int]
getInts = readInts <$> C.getLine
